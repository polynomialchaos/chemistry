def parse_input( mech_path, thermo_path=None, transport_path=None ):
    """Parse the provided paths."""

    tmp = parse_mechanism( mech_path )

    if thermo_path:
        tmp.thermos += parse_thermos( thermo_path )

    if transport_path:
        tmp.transports += parse_transports( transport_path )

    return tmp